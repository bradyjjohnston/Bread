void parseUARTBuffer(char *buffer, size_t len)
{
    char cmd[RX_BUFFER_SIZE];
    size_t cmdIndex = 0;
    bool inCommand = false;

    for (size_t i = 0; i < len; i++)
    {
        char c = buffer[i];

        if (c == 0x99 && i + 1 < len && buffer[i + 1] == MY_SLOT_ID) // If the loaf is requesting a packet from this slice
        {
            respondToLoaf();
            buffer[i] = '\0'; // Clear processed command
            buffer[i + 1] = '\0';
            i++;
        }
        else if (c == '>')
        {
            // If we were already building a command, finalize it
            if (inCommand && cmdIndex > 0)
            {
                cmd[cmdIndex] = '\0';
         //       processCommand(cmd);
                cmdIndex = 0;
            }

            // Start a new command
            inCommand = true;
            buffer[i] = '\0'; // Clear processed command
        }
        else if (c == '\n')
        {
            // End of packet — flush any active command
            if (inCommand && cmdIndex > 0)
            {
                cmd[cmdIndex] = '\0';
          //      processCommand(cmd);
            }

            // Reset state
            inCommand = false;
            cmdIndex = 0;
            buffer[i] = '\0'; // Clear processed command
        }
        else if (inCommand)
        {
            // Accumulate command characters safely
            if (cmdIndex < (RX_BUFFER_SIZE - 1))
            {
                cmd[cmdIndex++] = c;
                buffer[i] = '\0'; // Clear processed command
            }
            else
            {
                // Overflow protection: discard oversized command
                cmdIndex = 0;
                inCommand = false;
            }
        }
    }
}

